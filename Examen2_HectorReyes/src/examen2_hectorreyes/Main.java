    private void btn_aggUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aggUsuarioActionPerformed
        // TODO add your handling code here:
        if (jT_nombreUsuario.getText().equals("")
                || jT_contraseña.getText().equals("")
                || jT_nombre.getText().equals("")
                || jT_apellido.getText().equals("")
                || jT_telefono.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Porfavor llene todos los campos.");
        } else {
            boolean existe = false;
            administradorUsuario adminUs = new administradorUsuario("./usuarios.lab");
            adminUs.cargarArchivo();
            Usuario usuario = new Usuario(
                    jT_nombreUsuario.getText(),
                    jT_contraseña.getText(),
                    Integer.parseInt(jSp_calidadWifi.getValue().toString()),
                    jT_nombre.getText(),
                    jT_apellido.getText(),
                    jT_telefono.getText());
            for (int i = 0; i < adminUs.getUsuarios().size(); i++) {
                if (adminUs.getUsuarios().get(i).getNombreUsuario().equals(usuario.getNombreUsuario())) {
                    JOptionPane.showMessageDialog(this, "El nombre de usuario ya existe, intenta con otro.");
                    existe = true;
                }
            }//fin for
            if (existe == false) {
                adminUs.añadirUsuario(usuario);
                adminUs.escribirArchivo();
                usuarioActual = usuario;
                JOptionPane.showMessageDialog(this, "Se ha registrado con éxito.");
                jD_registrarUsuario.dispose();
                jD_login.dispose();
            }
        }
    }//GEN-LAST:event_btn_aggUsuarioActionPerformed

    private void btn_buscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarUsuarioActionPerformed
        // TODO add your handling code here:
        administradorUsuario adminUs = new administradorUsuario("./usuarios.lab");
        adminUs.cargarArchivo();
        boolean existe = false;
        limpiarTablaBuscarAmigos();
        for (int i = 0; i < adminUs.getUsuarios().size(); i++) {
            if (!adminUs.getUsuarios().get(i).getNombreUsuario().equals(usuarioActual.getNombreUsuario())) {
                if (jT_buscarAmigo.getText().equals(adminUs.getUsuarios().get(i).getNombreUsuario())) {
                    DefaultTableModel model = (DefaultTableModel) jTbl_aggUsuario.getModel();
                    String[] nRow = {adminUs.getUsuarios().get(i).getNombreUsuario()};
                    model.addRow(nRow);
                    existe = true;
                    jTbl_aggUsuario.setModel(model);
                }//fin if
            }//fin if
        }//fin for
        if (existe == false) {
            JOptionPane.showMessageDialog(this, "No se ha encontrado ningún usuario.");
        }
    }//GEN-LAST:event_btn_buscarUsuarioActionPerformed

    private void btn_ingresarLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarLoginActionPerformed
        // TODO add your handling code here:
        if (jT_usuarioLogin.getText().equals("")
                || jT_contraseñaLogin.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Porfavor llene todos los campos.");
        } else {
            boolean login = false;
            administradorUsuario adminUs = new administradorUsuario("./usuarios.lab");
            adminUs.cargarArchivo();
            for (int i = 0; i < adminUs.getUsuarios().size(); i++) {
                if (adminUs.getUsuarios().get(i).getNombreUsuario().equals(jT_usuarioLogin.getText())
                        && adminUs.getUsuarios().get(i).getContraseña().equals(jT_contraseñaLogin.getText())) {
                    usuarioActual = adminUs.getUsuarios().get(i);
                    login = true;
                    jMI_aggAmigos.setEnabled(true);
                    jD_login.dispose();
                    JOptionPane.showMessageDialog(this, "Bienvenido.");
                }
            }//fin for
            if (login == false) {
                JOptionPane.showMessageDialog(this, "Nombre de usuario o contraseña incorrectos.");
            }
        }
    }//GEN-LAST:event_btn_ingresarLoginActionPerformed

    private void btn_enviarSolicitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarSolicitudActionPerformed
        // TODO add your handling code here:
        if (enviarSolicitud == true) {
            administradorUsuario adminUs = new administradorUsuario("./usuarios.lab");
            adminUs.cargarArchivo();
            String nameUsuario = jTbl_aggUsuario.getValueAt(0, 0).toString();
            for (int i = 0; i < adminUs.getUsuarios().size(); i++) {
                if (adminUs.getUsuarios().get(i).getNombreUsuario().equals(nameUsuario)) {
                    if (!adminUs.getUsuarios().get(i).getSolicitudes().contains(usuarioActual)) {//si falla borrar
                        adminUs.getUsuarios().get(i).getSolicitudes().add(usuarioActual);
                        JOptionPane.showMessageDialog(this, "Se ha enviado una solicitud.");
                    } else {
                        JOptionPane.showMessageDialog(this, "Ya le has enviado una solicitud.");
                    }
                }
            }
        }
    }//GEN-LAST:event_btn_enviarSolicitudActionPerformed

