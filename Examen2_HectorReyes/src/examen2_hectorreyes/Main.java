    public void cargarTablaSolicitudes() {
        limpiarTablaSolicitudes();
        DefaultTableModel model = (DefaultTableModel) jTbl_solicitudes.getModel();
        for (int i = 0; i < usuarioActual.getSolicitudes().size(); i++) {
            String nRow[] = {usuarioActual.getSolicitudes().get(i).getNombreUsuario()};
            model.addRow(nRow);
        }
        jTbl_solicitudes.setModel(model);
    }

